function splitSeats(seats: any[], zone: any): any[] {
  return seats.map((seat: any) => {
    let zoneName = zone;
    let seatNum = seat.seat_number;
    let name = seat.seat_number;
    // seat_numberê°€ 'VIP-A1' ë“± '-' í¬í•¨ ì‹œ ë¶„ë¦¬
    if (seat.seat_number.includes("-")) {
      const parts = seat.seat_number.split("-");
      if (parts.length === 2) {
        zoneName = parts[0];
        seatNum = parts[1];
      } else if (parts.length > 2) {
        zoneName = parts.slice(0, -1).join("-");
        seatNum = parts[parts.length - 1];
      }
    }
    return {
      zone: zoneName,
      seat_number: seatNum,
      name,
      seat_status: seat.seat_status,
    };
  });
}

